    ret nz
    add a,0x20
    ret

; The 'final code' values for tokens are found by adding +A5 to the 'main code'.

k_tokens:
    add a,0xa5
    ret

; Next the digit keys; and SPACE, ENTER & both shifts; are considered.

k_digit:
    cp 0x30
    ret c
    dec c
    db 0xfa,0x9d,0x03,0x20,0x19
